function t(key) {
    return (TRANSLATIONS[currentLang] || TRANSLATIONS.en)[key]
        || TRANSLATIONS.en[key] || key;
}

function setLanguage(code) {
    if (!TRANSLATIONS[code]) return;
    currentLang = code;
    const lang = TRANSLATIONS[code];
    document.documentElement.setAttribute('dir', lang.dir);
    document.documentElement.setAttribute('lang', lang.lang);
    document.body.setAttribute('data-lang', code);
    try { localStorage.setItem('icna-lang', code); } catch (e) { }
    document.querySelectorAll('.lang-btn').forEach(b =>
        b.classList.toggle('active', b.dataset.lang === code));
    if (typeof applyTranslations === 'function') applyTranslations(lang);
}

function restoreLanguage() {
    try { setLanguage(localStorage.getItem('icna-lang') || 'en'); }
    catch (e) { setLanguage('en'); }
}
